fun main() {

    // var for variables are used for variables that are subject to change
    var firstName = "David"
    var lastName= "Gilson"

    // val is user for variables which are not set to change (e.g. constants)
    val age = 19
    val decimal = 3.1415

    println("\nHello world, my first name is $firstName and my last name is $lastName and I am $age years old \n")
    println("I also have some decimals: $decimal")

}
